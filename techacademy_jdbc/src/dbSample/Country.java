package dbSample;

public class Country {
    // プロパティ
    private String name;
    private Integer population;

    // getter および setter
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public Integer getPopulation() {
        return population;
    }
    public void setPopulation(Integer population) {
        this.population = population;
    }
}
